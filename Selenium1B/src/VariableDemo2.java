

// it is a sample java program where we tried to print values if it was represented using ("")
// inside the println statement then the value which was written the same will be printed example
//("5+5") we get (5+5). if we have given a println statement without ("") then it consider it as
// a variable. using 'a' variable in the below case.


public class VariableDemo2 {

	public static void main(String[] args) {
		
		System.out.println("5");
		System.out.println(5);
		System.out.println("5+5");
		System.out.println(5+5);
		
		int a = 5;
		System.out.println("a");
		System.out.println(a);
		System.out.println("a+5");
		System.out.println(a+5);
  		
		

	}

}
