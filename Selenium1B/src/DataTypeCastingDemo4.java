// By default all numerical values can be considered as 'int' data type. In java 
//By default all decimal values can b e considered as 'double' data type.

//we will be able to be differentiating between int, long, float, and double using suffixes.
//The suffixes (L for long, f for float)

public class DataTypeCastingDemo4 {

	public static void main(String[] args) {
	int a = 10;// '10' is by default an int
	long b = 10l;// '10L' is explicitly a long
	float c = 10.23f;//'10.23f' is explicitly a float
	 double d = 10.23;//'10.23' is by default a double
	 
	}

}
